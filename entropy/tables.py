# Table 9-5(d): nC >= 8 (fixed length code)
# For nC >= 8, use 6-bit fixed length code
# code = (TrailingOnes << 4) | (TotalCoeff - 1) for TotalCoeff > 0
# code = 3 for TotalCoeff = 0

def get_coeff_token_table(nC: int) -> dict:
    """Get coeff_token table for given nC value."""
    if nC < 2:
        return COEFF_TOKEN_0
    elif nC < 4:
        return COEFF_TOKEN_2
    elif nC < 8:
        return COEFF_TOKEN_4
    else:
        return None  # Use fixed length for nC >= 8


# Build reverse lookup tables for decoding
def _build_coeff_token_decode_table(encode_table: dict) -> dict:
    """Build decode table: (code, num_bits) -> (TotalCoeff, TrailingOnes)."""
    decode = {}
    for (tc, t1), (code, bits) in encode_table.items():
        decode[(code, bits)] = (tc, t1)
    return decode

COEFF_TOKEN_DECODE_0 = _build_coeff_token_decode_table(COEFF_TOKEN_0)
COEFF_TOKEN_DECODE_2 = _build_coeff_token_decode_table(COEFF_TOKEN_2)
COEFF_TOKEN_DECODE_4 = _build_coeff_token_decode_table(COEFF_TOKEN_4)


