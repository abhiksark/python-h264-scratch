# Table 9-5d: 8 <= nC (fixed-length)
# Uses 6-bit fixed-length codes
# (TotalCoeff, TrailingOnes) -> 6-bit code = (TrailingOnes << 4) | (TotalCoeff - 1 if TotalCoeff > 0 else 0)
# For (0, 0): code = 0b000011

def build_spec_coeff_token_8():
    """Build Table 9-5d for nC >= 8."""
    result = {}
    # TotalCoeff = 0
    result[(0, 0)] = (0b000011, 6)
    # TotalCoeff > 0
    for tc in range(1, 17):
        for t1 in range(min(tc, 3) + 1):
            code = (t1 << 4) | (tc - 1)
            result[(t1, tc)] = (code, 6)
    return result

SPEC_COEFF_TOKEN_8 = build_spec_coeff_token_8()


def verify_table(name, our_table, spec_table):
    """Verify our table against spec."""
    print(f"\n=== {name} ===")
    all_match = True
    errors = []

    # Check for duplicate codes in our table
    code_to_entry = {}
    for (t1, tc), (code, length) in our_table.items():
        key = (code, length)
        if key in code_to_entry:
            other = code_to_entry[key]
            errors.append(f"DUPLICATE code ({code:0{length}b}, {length}) for ({t1},{tc}) and {other}")
            all_match = False
        code_to_entry[key] = (t1, tc)

    # Compare entries
    for (t1, tc) in sorted(spec_table.keys()):
        spec_code, spec_len = spec_table[(t1, tc)]

        if (t1, tc) not in our_table:
            errors.append(f"MISSING ({t1},{tc}): spec has ({spec_code:0{spec_len}b}, {spec_len})")
            all_match = False
            continue

        our_code, our_len = our_table[(t1, tc)]

        if (our_code, our_len) != (spec_code, spec_len):
            errors.append(f"MISMATCH ({t1},{tc}): spec=({spec_code:0{spec_len}b}, {spec_len}), ours=({our_code:0{our_len}b}, {our_len})")
            all_match = False

    # Check for extra entries
    for (t1, tc) in our_table:
        if (t1, tc) not in spec_table:
            our_code, our_len = our_table[(t1, tc)]
            errors.append(f"EXTRA ({t1},{tc}) in our table: ({our_code:0{our_len}b}, {our_len})")

    if errors:
        for err in errors:
            print(f"  {err}")
    else:
        print("  All entries match!")

    return all_match


def main():
    from entropy.tables import COEFF_TOKEN_0, COEFF_TOKEN_2, COEFF_TOKEN_4

    all_match = True

    all_match &= verify_table("COEFF_TOKEN_0 (nC 0-1)", COEFF_TOKEN_0, SPEC_COEFF_TOKEN_0)
    all_match &= verify_table("COEFF_TOKEN_2 (nC 2-3)", COEFF_TOKEN_2, SPEC_COEFF_TOKEN_2)
    all_match &= verify_table("COEFF_TOKEN_4 (nC 4-7)", COEFF_TOKEN_4, SPEC_COEFF_TOKEN_4)
    # nC >= 8 uses fixed-length codes handled directly in cavlc.py

    if all_match:
        print("\n\nAll COEFF_TOKEN tables verified!")
    else:
        print("\n\nSome tables have errors.")


if __name__ == '__main__':
    main()
