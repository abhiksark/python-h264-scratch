def format_table(table, name):
    """Format table as Python code."""
    lines = [f"{name} = {{"]
    # Sort by (TC, T1)
    for (tc, t1), (code, length) in sorted(table.items()):
        lines.append(f"    ({tc}, {t1}): (0b{code:0{length}b}, {length}),")
    lines.append("}")
    return "\n".join(lines)


